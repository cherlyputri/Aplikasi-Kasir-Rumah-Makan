    private void btn_pesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesanActionPerformed
        input ip = new input();

        ip.pesanan = (String) cbx_pesanan.getSelectedItem();
        ip.harga = txt_harga.getText();
        ip.jumlah = txt_jumlah.getText();
        ip.nomeja = (String) cbx_nomeja.getSelectedItem();
        ip.pemesan = txt_pemesan.getText();
        ip.jumlah_orang = txt_jumlah_orang.getText();
        ip.jenis = (String) cbx_jenis.getSelectedItem();
        ip.total = txt_total.getText();
        ip.bayar = txt_bayar.getText();
        ip.kembalian = txt_kembali.getText();

        try {
            ip.simpan();
            JOptionPane.showMessageDialog(null, "Pesanan Tersimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
        ViewData();
        cbx_pesanan.setSelectedItem("");
        txt_harga.setText("");
        txt_jumlah.setText("");
        txt_bayar.setText("");
        txt_kembali.setText("");
        cbx_nomeja.setSelectedItem("");
        txt_pemesan.setText("");
        txt_jumlah_orang.setText("");
        cbx_jenis.setSelectedItem("");
        txt_total.setText("");


    }//GEN-LAST:event_btn_pesanActionPerformed

