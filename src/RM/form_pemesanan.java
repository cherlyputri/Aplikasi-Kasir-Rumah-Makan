    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int i = tabel.getSelectedRow();

        if (i > -1) {
            cbx_pesanan.setSelectedItem(tabel.getValueAt(i, 0).toString());
            txt_harga.setText(tabel.getValueAt(i, 1).toString());
            txt_jumlah.setText(tabel.getValueAt(i, 2).toString());
            cbx_nomeja.setSelectedItem(tabel.getValueAt(i, 3).toString());
            txt_pemesan.setText(tabel.getValueAt(i, 4).toString());
            txt_jumlah_orang.setText(tabel.getValueAt(i, 5).toString());
            cbx_jenis.setSelectedItem(tabel.getValueAt(i, 6).toString());
            txt_total.setText(tabel.getValueAt(i, 7).toString());
            txt_bayar.setText(tabel.getValueAt(i, 8).toString());
            txt_kembali.setText(tabel.getValueAt(i, 9).toString());
        }
    }//GEN-LAST:event_tabelMouseClicked

